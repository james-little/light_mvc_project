        '[', ']', '{', '}', '\\',
        '|', '+', '=', '_', '^',
        '$', '~', '`'
    );
    /**
     * convert full width Japanese words to half width
     * @param string $string
     * @return string
     */
    static public function convertHalfWidth($string) {
        return str_replace(self::$DBC, self::$SBC, $string);
    }
}